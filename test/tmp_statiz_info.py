def stadium_name(name='MH'):
   if name == 'MH':
      return '인천문학'
   elif name == 'JS':
      return '잠실'
   elif name == 'SJ':
      return '부산사직'
   elif name == 'DK':
      return '라이온즈파크'
   elif name == 'KC':
      return '챔피언스필드'
   elif name == 'CW':
      return '창원NC파크'
   elif name == 'SW':
      return '케이티위즈파크'
   elif name == 'GC':
      return '고척돔'
   elif name == 'DJ':
      return '대전한밭'
   else:
      return '천연잔디'

